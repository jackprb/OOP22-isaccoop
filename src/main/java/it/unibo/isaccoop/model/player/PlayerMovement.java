package it.unibo.isaccoop.model.player;

/**
 * Interface for the player movement.
 * */
public interface PlayerMovement {

    /**
     * Move the player to right.
     * */
    void right();

    /**
     * Move the player to left.
     * */
    void left();

    /**
     * Move the player up.
     * */
    void up();

    /**
     * Move the player down.
     * */
    void down();

}
